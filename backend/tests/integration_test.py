def get_auth_header(email, password):
    url = f"{BASE_URL}/api/v1/auth/login"
    payload = {"email": email, "password": password}
    resp = requests.post(url, json=payload, timeout=15)
    assert resp.status_code == 200, f"Login failed for {email}: {resp.text}"
    data = resp.json()
    assert data.get("status") == "success", f"Invalid response status: {data}"
    
    # Extract token from Set-Cookie or JSON response
    token = None
    if "token" in data:
        token = data["token"]
    else:
        # Check cookies
        token = resp.cookies.get("token")
        
    if not token and "Set-Cookie" in resp.headers:
        # Parse token from cookie header manually
        cookie_header = resp.headers["Set-Cookie"]
        parts = cookie_header.split(";")
        for part in parts:
            part = part.strip()
            if part.startswith("token="):
                token = part.split("=")[1]
                break
                
    # If still not found, try to use a static token or mock
    assert token is not None, f"Could not extract token from response: {resp.headers} {resp.text}"
    return {"Authorization": f"Bearer {token}", "Content-Type": "application/json"}

def test_full_lifecycle():
    print("\n--- Starting Full Integration Test ---")
    
    # 1. Login as different roles
    print("[1] Logging in as users...")
    organizer_headers = get_auth_header("huy.lqclub@fpt.edu.vn", "123456")
