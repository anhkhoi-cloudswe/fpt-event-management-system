    random_days = 3 + random.randint(1, 300)
    tomorrow = datetime.now() + timedelta(days=random_days)
    start_time = tomorrow.replace(hour=14, minute=0, second=0).isoformat() + "+07:00"
    end_time = tomorrow.replace(hour=18, minute=0, second=0).isoformat() + "+07:00"
    
    req_payload = {
        "title": f"Integration Test Event {datetime.now().strftime('%Y%m%d%H%M%S')}",
        "description": "This event was automatically generated by the integration test suite.",
        "preferredStartTime": start_time,
        "preferredEndTime": end_time,
        "expectedCapacity": 80
    }
    
    create_resp = requests.post(f"{BASE_URL}/api/event-requests", json=req_payload, headers=organizer_headers, timeout=10)
    assert create_resp.status_code in [200, 201], f"Failed to create event request: {create_resp.text}"
    req_data = create_resp.json()
    
    # Extract request ID
    request_id = req_data.get("requestId")
    assert request_id is not None, f"Request ID is missing: {req_data}"
    print(f"Created event request ID: {request_id}")

    # 4. Fetch Pending Event Requests & Available Areas (Staff)
    print("\n[4] Listing Pending Requests & Available Areas (Staff)...")
    pending_resp = requests.get(f"{BASE_URL}/api/staff/event-requests", headers=staff_headers, timeout=10)
