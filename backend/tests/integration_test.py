    areas_url = f"{BASE_URL}/api/events/available-areas?startTime={start_time}&endTime={end_time}&expectedCapacity=80"
    areas_resp = requests.get(areas_url, headers=staff_headers, timeout=10)
    assert areas_resp.status_code == 200, f"Failed to list available areas: {areas_resp.text}"
    areas_data = areas_resp.json()
    areas = areas_data.get("availableAreas", [])
    assert len(areas) > 0, f"No available areas found for the requested time slot! Response: {areas_data}"
    area_id = areas[0]["areaId"]
    print(f"Found available area ID: {area_id}")

    # 5. Process / Approve Event Request (Staff)
    print("\n[5] Approving Event Request (Staff)...")
    approve_payload = {
        "requestId": request_id,
        "action": "APPROVED",
        "organizerNote": "Yeu cau da duoc duyet thanh cong.",
        "areaId": area_id,
        "speakerId": None,
        "bannerUrl": "https://images.unsplash.com/photo-1511578314322-379afb476865"
    }
    process_resp = requests.post(f"{BASE_URL}/api/event-requests/process", json=approve_payload, headers=staff_headers, timeout=15)
    # Check for HTTP 500 error specifically
    assert process_resp.status_code != 500, f"CRITICAL 500 ERROR: {process_resp.text}"
    assert process_resp.status_code == 200, f"Failed to process event request: {process_resp.text}"
    
    # Fetch request detail to retrieve the generated event ID
    print("Fetching updated request details to get createdEventId...")
    time.sleep(1)
    req_detail_resp = requests.get(f"{BASE_URL}/api/event-requests/{request_id}", headers=organizer_headers, timeout=10)
    assert req_detail_resp.status_code == 200, f"Failed to get event request details: {req_detail_resp.text}"
    req_detail = req_detail_resp.json()
    created_event_id = req_detail.get("createdEventId")
    assert created_event_id is not None, f"createdEventId is missing in request details: {req_detail}"
    print(f"Event successfully approved and created with Event ID: {created_event_id}")

    # 6. Update Event Request Details / Initialize Seats (Organizer)
    print("\n[6] Updating Event Request Details - Re-initializing Seats (Organizer)...")
    # This was throwing HTTP 500 with pg syntax error at or near "\""
    update_payload = {
        "requestId": request_id,
        "eventId": created_event_id,
        "status": "UPDATING",
        "speaker": {
            "fullName": "Dien gia kiem thu",
            "bio": "Bio kiem thu",
            "email": "diengia@gmail.com",
            "phone": "0987654321",
            "avatarUrl": "https://avatar.url"
        },
        "tickets": [
            {
                "name": "Ve Thuong",
                "description": "Ve kiem thu tu dong",
                "price": 50000,
                "maxQuantity": 40
            },
            {
                "name": "Ve VIP",
                "description": "Ve VIP kiem thu",
                "price": 150000,
                "maxQuantity": 10
            }
        ],
        "bannerUrl": "https://images.unsplash.com/photo-1511578314322-379afb476865",
        "dryRun": False
    }
    
    update_resp = requests.post(f"{BASE_URL}/api/event-requests/update", json=update_payload, headers=organizer_headers, timeout=15)
    # Catch any SQL syntax issues or 500 errors
    assert update_resp.status_code != 500, f"CRITICAL 500 ERROR during seat re-initialization: {update_resp.text}"
    assert update_resp.status_code == 200, f"Failed to update event request: {update_resp.text}"
    print("Event request successfully updated! Seats initialized correctly without PostgreSQL errors.")

